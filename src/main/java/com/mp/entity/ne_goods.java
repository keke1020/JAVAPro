package com.mp.entity;

public class ne_goods {
	private String code;
	private String dhcode;
	private String name;
	private String baika;
	private String genka;
	private String teika;
	private String tag;

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public String getDhcode() {
		return dhcode;
	}

	public void setDhcode(String dhcode) {
		this.dhcode = dhcode;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getBaika() {
		return baika;
	}

	public void setBaika(String baika) {
		this.baika = baika;
	}

	public String getGenka() {
		return genka;
	}

	public void setGenka(String genka) {
		this.genka = genka;
	}

	public String getTeika() {
		return teika;
	}

	public void setTeika(String teika) {
		this.teika = teika;
	}

	public String getTag() {
		return tag;
	}

	public void setTag(String tag) {
		this.tag = tag;
	}

}
